package br.com.zupandroid.dadosdousuario;

public class MensagemError {
    public String logName, logLastName, logTelephone, logCelphone, logCpf, logSchool, logNeighborhood, logStats;

    public MensagemError() {
    }

    public String getLogName() {
        return logName;
    }

    public void setLogName(String logName) {
        this.logName = logName;
    }

    public String getLogLastName() {
        return logLastName;
    }

    public void setLogLastName(String logLastName) {
        this.logLastName = logLastName;
    }

    public String getLogTelephone() {
        return logTelephone;
    }

    public void setLogTelephone(String logTelephone) {
        this.logTelephone = logTelephone;
    }

    public String getLogCelphone() {
        return logCelphone;
    }

    public void setLogCelphone(String logCelphone) {
        this.logCelphone = logCelphone;
    }

    public String getLogCpf() {
        return logCpf;
    }

    public void setLogCpf(String logCpf) {
        this.logCpf = logCpf;
    }

    public String getLogSchool() {
        return logSchool;
    }

    public void setLogSchool(String logSchool) {
        this.logSchool = logSchool;
    }

    public String getLogNeighborhood() {
        return logNeighborhood;
    }

    public void setLogNeighborhood(String logNeighborhood) {
        this.logNeighborhood = logNeighborhood;
    }

    public String getLogStats() {
        return logStats;
    }

    public void setLogStats(String logStats) {
        this.logStats = logStats;
    }
}
